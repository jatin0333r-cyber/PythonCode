def main():
    inc = int(input("Enter income in Rs: "))
    tax = 0
    if inc <= 250000:
        tax = 0
    elif inc <= 500000:
        tax = (inc - 250000) * 0.05
    elif inc <= 1000000:
        tax = (250000 * 0) + (250000 * 0.05) + (inc - 500000) * 0.10
    elif inc <= 2000000:
        tax = (250000 * 0) + (250000 * 0.05) + (500000 * 0.10) + (inc - 1000000) * 0.20
    elif inc <= 3000000:
        tax = (250000 * 0) + (250000 * 0.05) + (500000 * 0.10) + (1000000 * 0.20) + (inc - 2000000) * 0.30
    else:
        tax = (250000 * 0) + (250000 * 0.05) + (500000 * 0.10) + (1000000 * 0.20) + (1000000 * 0.30) + (inc - 3000000) * 0.40
    print("Tax: Rs.", round(tax, 2))


if __name__ == "__main__":
    main()

